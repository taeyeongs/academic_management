package DTO;

public class Classroom {
	private int classroomNo;
	private String classroomName;
	private int classroomPersonnel;
	public int getClassroomNo() {
		return classroomNo;
	}
	public void setClassroomNo(int classroomNo) {
		this.classroomNo = classroomNo;
	}
	public String getClassroomName() {
		return classroomName;
	}
	public void setClassroomName(String classroomName) {
		this.classroomName = classroomName;
	}
	public int getClassroomPersonnel() {
		return classroomPersonnel;
	}
	public void setClassroomPersonnel(int classroomPersonnel) {
		this.classroomPersonnel = classroomPersonnel;
	}
}
