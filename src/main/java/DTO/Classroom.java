package DTO;

public class Classroom {
	private int classroom_no;
	private String classroom_name;
	private int classroom_personnel;
	public int getClassroom_no() {
		return classroom_no;
	}
	public void setClassroom_no(int classroom_no) {
		this.classroom_no = classroom_no;
	}
	public String getClassroom_name() {
		return classroom_name;
	}
	public void setClassroom_name(String classroom_name) {
		this.classroom_name = classroom_name;
	}
	public int getClassroom_personnel() {
		return classroom_personnel;
	}
	public void setClassroom_personnel(int classroom_personnel) {
		this.classroom_personnel = classroom_personnel;
	}
	
	
}
